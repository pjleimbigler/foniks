// Export a function to get emojis filtered by difficulty
export const getEmojisByDifficulty = (difficulty) => {
  if (difficulty === 'all') {
    return WORD_EMOJIS;
  }
  
  return Object.entries(WORD_EMOJIS)
    .filter(([_, data]) => data.difficulty === difficulty)
    .reduce((acc, [word, data]) => {
      acc[word] = data;
      return acc;
    }, {});
}; 